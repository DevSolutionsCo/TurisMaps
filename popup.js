

//Acciones de las alcadias
var btn_abrir_azca = document.getElementById("btn_abrir_azca"),
    overlay_azca = document.getElementById("overlay_azca"),
    popup_azca = document.getElementById("popup_azca"),
    cerrar_alca_azca = document.getElementById("cerrar_alca_azca");

btn_abrir_azca.addEventListener('mousedown', function(){
    overlay_azca.classList.add('active');
    popup_azca.classList.add('active');
});

cerrar_alca_azca.addEventListener('click' ,function(){
    overlay_azca.classList.remove('active');
    popup_azca.classList.remove('active');
});



